    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        selectedRow = tblPersonDirectory.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Person person = personDirectory.getTemporaryPersonDirectory().get(selectedRow);
        personDirectory.getTemporaryPersonDirectory().remove(person);
        personDirectory.getPersonDirectory().remove(person);
        populateTableSearched();
    }//GEN-LAST:event_btnDeleteActionPerformed


