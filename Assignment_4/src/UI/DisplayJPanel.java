    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if (boxSearchName.getSelectedItem().toString().equals("First Name") && !txtSearch.getText().isEmpty()){
            //JOptionPane.showMessageDialog(null, "Please Enter Car Model Number!!", "Warning", JOptionPane.WARNING_MESSAGE);
            personDirectory.clearTable();
            personDirectory.searchFirstName(txtSearch.getText());
            populateTableSearched();
        }else if(boxSearchName.getSelectedItem().toString().equals("Last Name") && !txtSearch.getText().isEmpty()){
            personDirectory.clearTable();
            personDirectory.searchLastName(txtSearch.getText());
            populateTableSearched();
        }else if(boxSearchName.getSelectedItem().toString().equals("ALL") && txtSearch.getText().isEmpty()){
            personDirectory.clearTable();
            personDirectory.copyToTemporary();
            populateTableSearched();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

