    private void populateTableSearched(){
        dtm = (DefaultTableModel) tblPersonDirectory.getModel();
        dtm.setRowCount(0);
        
        for(Person person: personDirectory.getTemporaryPersonDirectory()){
            Object[] row = new Object[10];
            row[0] = person.getFirstName();
            row[1] = person.getLastName();
            row[2] = person.getGender();
            row[3] = person.getAge();
            row[4] = person.getHouse().getAddress();
            row[5] = person.getHouse().getCommunity().getCommunityName();
            row[6] = person.getHouse().getCommunity().getCity().getCityName();
            dtm.addRow(row);
        }
    }

